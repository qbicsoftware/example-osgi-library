package life.qbic.example.osgi.library;

import java.util.Random;

/**
 * Example class with a static method that can be used to simulate the data transfer between
 * this OSGi library bundle and another OSGi bundle which uses this library as dependency.
 */
public class LibraryExample {

    /**
     * Creates a message including a random number, the fully-qualified class name an the
     * ClassLoader instance.
     *
     * @return String message including random number, fully-qualified class name
     *         and ClassLoader instance
     */
    public static String getRandomNumberMessage() {
        int number = new Random().nextInt();

        String message = String.format("Random Number: %d \nGenerated by %s \nLoaded by %s",
                number, LibraryExample.class, LibraryExample.class.getClassLoader());

        System.out.println(message);

        return message;
    }
}
